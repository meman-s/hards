"""
Задание: Первый неповторяющийся символ
Строка из строчных латинских букв. Найди индекс первого символа,
который встречается в строке ровно один раз. Если такого нет — верни -1.
Пример: "leetcode" → 0, "aabb" → -1.

Совет: один проход — посчитать частоты (dict или Counter); второй проход — найти первый с частотой 1.
Или один проход с индексом: храни для каждой буквы (счётчик, первый индекс).
Библиотеки: collections.Counter(s) даёт частоты символов за один проход — идеально для этой задачи.
"""
from collections import Counter
string = input()

counter = Counter(string)
for i, letter in enumerate(string):
    if counter[letter] == 1:
        print(i)
        break
else:
    print(-1)
